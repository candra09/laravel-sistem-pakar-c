        <h2 class="section-title">Penyakit GERD</h2>
        <p>Gastroesophageal Reflux Disease (GERD) adalah kondisi medis di mana asam lambung secara kronis mengalir kembali ke esofagus, yang menyebabkan iritasi pada lapisan esofagus.</p>

        <h2 class="section-title">Penyebab GERD</h2>
        <ul>
            <li>Kelebihan Berat Badan: Tekanan ekstra pada perut dapat menyebabkan asam lambung naik.</li>
            <li>Makanan dan Minuman: Konsumsi makanan berlemak, pedas, cokelat, kafein, alkohol, dan minuman berkarbonasi.</li>
            <li>Merokok: Merokok dapat melemahkan katup antara perut dan esofagus.</li>
            <li>Kehamilan: Perubahan hormon dan tekanan pada perut selama kehamilan.</li>
            <li>Hernia Hiatus: Bagian atas perut yang menonjol melalui diafragma ke dalam rongga dada.</li>
            <li>Obat-obatan: Beberapa obat dapat melemahkan katup esofagus atau menyebabkan iritasi esofagus.</li>
        </ul>

        <h2 class="section-title">Gejala GERD</h2>
        <ul>
            <li>Heartburn: Rasa terbakar di dada, biasanya setelah makan dan bisa memburuk saat berbaring atau membungkuk.</li>
            <li>Regurgitasi: Asam lambung atau makanan yang naik kembali ke mulut.</li>
            <li>Kesulitan Menelan (Disfagia).</li>
            <li>Sakit Dada: Bisa terasa mirip dengan nyeri dada akibat serangan jantung.</li>
            <li>Batuk Kering Kronis.</li>
            <li>Suara Serak atau Radang Tenggorokan.</li>
            <li>Perasaan ada benjolan di tenggorokan.</li>
        </ul>

        <h2 class="section-title">Solusi untuk GERD</h2>
        <h4>Perubahan Gaya Hidup:</h4>
        <ul>
            <li>Menurunkan berat badan jika kelebihan berat badan.</li>
            <li>Menghindari makanan dan minuman yang memicu gejala.</li>
            <li>Tidak berbaring segera setelah makan.</li>
            <li>Makan dalam porsi kecil namun sering.</li>
        </ul>
        <h4>Obat-obatan:</h4>
        <ul>
            <li>Antasida untuk menetralisir asam lambung.</li>
            <li>Penghambat reseptor H2 untuk mengurangi produksi asam lambung.</li>
            <li>Penghambat pompa proton (PPI) untuk mengurangi produksi asam lambung lebih efektif.</li>
        </ul>
        <h4>Tindakan Medis:</h4>
        <ul>
            <li>Fundoplikasi Nissen: Prosedur bedah untuk memperkuat katup antara perut dan esofagus.</li>
            <li>LINX device: Cincin magnetik kecil yang ditempatkan di sekitar bagian bawah esofagus untuk mencegah refluks.</li>
        </ul>

        <h2 class="section-title">Cara Mencegah GERD</h2>
        <ul>
            <li>Menjaga Berat Badan Ideal: Mengurangi tekanan pada perut.</li>
            <li>Menghindari Makanan dan Minuman Pemicu: Seperti makanan pedas, berlemak, cokelat, alkohol, dan minuman berkarbonasi.</li>
            <li>Tidak Merokok: Merokok dapat memperburuk GERD.</li>
            <li>Menghindari Makan Besar Sebelum Tidur: Memberikan waktu pencernaan minimal 3 jam sebelum berbaring.</li>
            <li>Mengangkat Kepala Tempat Tidur: Meninggikan kepala tempat tidur sekitar 15-20 cm untuk mencegah asam lambung naik saat tidur.</li>
            <li>Memakai Pakaian Longgar: Menghindari pakaian ketat yang dapat menekan perut.</li>
        </ul>
